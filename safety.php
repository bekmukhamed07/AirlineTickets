<b>Points to remember for your safety</b>
<br/>
<br/>
<b>General</b>
<br/><p>	Always follow crew members instructions and comply with lighted signs and posted placards on the aircraft</p>
<br/><p>	Do not smoke inside any part of the aircraft at any time and do not tamper with smoke detectors</p>
<br/><p>	Ensure that your luggage is not blocking the aisle or an exit </p>
<br/><p>	Your luggage may be stored under the seat in front of you or in an overhead locker compartment</p>
<br/><p>	Take care when placing luggage in overhead lockers to avoid injury to yourself or other passengers</p>
<br/><p>	Take care when opening overhead lockers at the end of the flight as luggage may have moved during flight</p>
<br/><p>	Keep your seat belt fastened when the seat belt sign is on or while you are seated throughout the flight</p> 
<br/><p>	If you are not in your seat when the seat belt sign is on return to your seat and fasten your seat belt</p>
<br/><p>	Note how to use the seat belt as indicated in the video or by cabin crew. This often involves clipping the two seat belt buckle parts together to fasten and lifting the buckle flap to release</p>
<br/><p>	Ensure that your seat belt is snug and fits low across your hips</p>
